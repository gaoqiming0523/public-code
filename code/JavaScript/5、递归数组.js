function deepRecursion(arr, deep = 0) {
    arr.forEach((item) => {
        console.log(`第${deep}层, ${item.value} ${item.label}`);
        if (item.children && item.children.length) {
            return deepRecursion(item.children, deep + 1);
        }
    });
}

